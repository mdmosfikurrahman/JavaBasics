package org.epde.cakeMango;

public class NumberInfo {
    private int number;
    private boolean isEven;

    public NumberInfo(int number, boolean isEven) {
        this.number = number;
        this.isEven = isEven;
    }

    public int getNumber() {
        return number;
    }

    public boolean isEven() {
        return isEven;
    }
}
